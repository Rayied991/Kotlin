package Datatypes_Variables

fun main() {
    //Kotlin uses Wrapper class Datatypes
    //Not the primitive datatypes
    var number:Double
    var str:String
    var check:Boolean
    var num:Byte
    var cr:Char
    number=100.3456
    str="Rayied"
    check=true
    num=17
    cr='A'

    println(number)
    println(str)
    println(check)
    println(num)
    println(cr)
    println(cr+2)
}